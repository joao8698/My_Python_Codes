# Tipo de dado bool (boolean)
'''
Ao questionar algo em um programa,
so existem duas respostas possiveis:
sim (True) ou nao (False).
'''
# Existem varios operadores para "questionar".
'''
Dentre eles o ==, que e um operador logico que
questiona se um valor e igual ao outro
'''
print(10 == 10) # Sim => True (Verdadeiro)
print(10 == 11) # Nao => False (Falso)
print(type(10 == 11)) # Tipo => type (tipo de dado)
print(type(10 == 10)) # Tipo => type (tipo de dado)
# Os dois prints com type como argumento vao pritar o tipo de dado inserido dentro da classe type, e o resultado vai ser o mesmo
# Tendo em vista que type mostra apenas o tipo de dado e nao o resultado da operacao que esta dentro dele
print(type(True))
print(type(False))