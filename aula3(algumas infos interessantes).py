'''
Python = Linguagem de programacao
Tipo de tipagem = Dinamica/Forte
str -> string -> texto
Strings sao textos que estao dentro de aspas
'''

# Aspas simples
print('Joao')

# Aspas duplas
print("Gaby")

# Escape
print('Gabrielly \'Joao\'')

# r
print(r'Ola \'Mundo\'')

# Jeito mas facil e mais bonito de colocar aspas(apenas inverta as aspas)
print('Ola "Mundo"')

# O Python ja sabe qual o tipo de informacao que voce esta escrevendo! (str, int, float, etc)
# Se fosse uma linguagem de programacao com tipagem estatica, voce teria que especificar qual o tipo de dado que voce esta inserindo