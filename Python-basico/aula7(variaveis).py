# Variaveis sao usadas para salvar algo na memoria do computador.
# PEP8: inicie variaveis com letras minusculas, pode usar
# numeros e underline _.
# O sinal de = e o operador de atribuicao. Ele e usado para
# atribuir um valor a um nome (variavel).
# Uso: nome_variavel = expressao
#      (snake_case)
soma_dois_mais_dois = 10 + 7
print(soma_dois_mais_dois)
nome_completo = 'Gabrielly Ensfeld Vieira'
print(nome_completo, soma_dois_mais_dois, sep=' - ')

