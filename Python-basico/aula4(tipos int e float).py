# Tipos int e float
# int -> Numero intereiro
# O tipo int representa qualquer numero
# positivo ou negativo. int sem sinal e considerado positivo.
print(11) # int
print(10) # int
print(0) # int
print(1, 2 ,3) # int
print(-1, -2, -3) # int

# float -> Numero com ponto flutuante
# O tipo float representa qualquer numero 
# positivo ou negativo com ponto flutuante.
# float sem sinal e considerado positivo.
print(10.5) # float
print(11.9) # float
print(1.1, 1.2, 1.3) # float
print(-1.1, -1.2, -1.3) # float

# A funcao type mostra o tipo que o python inferiu ao valor.
print(   type(1)   ) # A classe type mostra qual e o tipo de dado inserido como parametro
print(   type('1')   ) # A classe type mostra qual e o tipo de dado inserido como parametro
print(   type('ola')   ) # A classe type mostra qual e o tipo de dado inserido como parametro

